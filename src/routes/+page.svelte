<h2>Lær om lyd, bilde og video!</h2>
<p>Velkommen til Multimedia 101! Her kan du lære mer om spennende emner som digital lyd, bilder og video. 
    Dykk ned i den digitale verden og oppdag hvordan lyden digitaliseres, 
    kodes og komprimeres for å gi den beste lydopplevelsen. 
    <br><br>
    Utforsk også de forskjellige aspektene ved digitale bilder, 
    inkludert piksler, farger, oppløsning og komprimeringsteknikker som brukes for å bevare kvaliteten 
    samtidig som filstørrelsen reduseres. Og ikke glem å ta en titt på vår seksjon om digital video,
    hvor du kan lære om datarate, oppløsning, komprimeringsformater og hvordan videostrømming fungerer.
    Uansett om du er en nybegynner eller en entusiast, har vi ressurser og informasjon som passer for deg.
    <br><br>
    Når du er føler deg kjent med digitale medier kan du tar vår <a href="/quiz">QUIZ</a> for å sjekke hva du har lært
    på Multimedia 101. Klikk på ett av emnene i navigasjonsmenyen for å starte.</p>
